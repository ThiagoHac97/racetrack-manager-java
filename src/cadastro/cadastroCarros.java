    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        VeiculoDao veiDao = new VeiculoDao();    
        if((veiDao.verifPlaca(jftPlaca.getText()))==true){
            JOptionPane.showMessageDialog(null,"Essa Placa já foi registrada! \n","Erro", JOptionPane.ERROR_MESSAGE);
        }else{
            if(tfMarca.getText().equals("") || tfModelo.getText().equals("(  )      -    ") || jftPlaca.getText().equals("   .   .   -  ") || tfPotencia.getText().equals("")){
                    JOptionPane.showMessageDialog(null,"Todos os campos precisam ser preenchidos! \n","Erro", JOptionPane.ERROR_MESSAGE);
            }else{
            Veiculo veiculo = new Veiculo();
            veiculo.setMarca(tfMarca.getText());
            veiculo.setModelo(tfModelo.getText());
            veiculo.setCategoria((String) jcCategoria.getSelectedItem());
            veiculo.setNumeroPlaca(jftPlaca.getText());
            veiculo.setPotencia(tfPotencia.getText());
            veiculo.setImagem(getImagem());
            veiDao.inserir(veiculo);
            tfMarca.setText("");
            tfModelo.setText("");
            jftPlaca.setText("");
            tfPotencia.setText("");
            lbImagem.setIcon(null);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

